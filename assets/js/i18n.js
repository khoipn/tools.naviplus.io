  var currentLang = 'en';

  /* ── URL helpers ──────────────────────────────────────────────── */
  function urlLangSegment() {
    var seg = window.location.pathname.split('/').filter(Boolean)[0];
    return (seg && T[seg]) ? seg : null;
  }

  function realPath() {
    var parts = window.location.pathname.split('/').filter(Boolean);
    if (parts.length > 0 && T[parts[0]]) parts = parts.slice(1);
    return '/' + parts.join('/');
  }

  function syncUrl(lang) {
    var clean = realPath();
    var want  = (lang === 'en') ? clean : ('/' + lang + clean);
    if (!want) want = '/';
    if (want !== window.location.pathname) {
      history.replaceState(null, '', want);
    }
    var homeHref = (lang === 'en') ? '/' : '/' + lang + '/';
    document.querySelectorAll('.tools-back-link').forEach(function (a) { a.href = homeHref; });
  }

  window.getLangPrefix = function () {
    return currentLang === 'en' ? '' : ('/' + currentLang);
  };

  /* ── Core ─────────────────────────────────────────────────────── */
  window.t = function (key) {
    return (T[currentLang] && T[currentLang][key]) || (T.en && T.en[key]) || key;
  };

  function detectLang() {
    var fromUrl = urlLangSegment();
    if (fromUrl) return fromUrl;
    var saved = localStorage.getItem(LANG_KEY);
    if (saved && T[saved]) return saved;
    var nav = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
    var code = nav.split('-')[0];
    if (code === 'zh') return 'zh';
    return T[code] ? code : 'en';
  }

  function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var val = window.t(key);
      if (val && val !== key) el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-ph');
      var val = window.t(key);
      if (val && val !== key) el.placeholder = val;
    });
    var flagEl = document.getElementById('np-lang-flag');
    if (flagEl) flagEl.textContent = FLAGS[currentLang] || FLAGS.en;
    document.querySelectorAll('[data-lang]').forEach(function (el) {
      el.classList.toggle('active', el.getAttribute('data-lang') === currentLang);
    });
    document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : currentLang;
    syncUrl(currentLang);
    if (typeof window.renderTools === 'function') window.renderTools();
    if (typeof window.renderToolTabs === 'function') window.renderToolTabs();
    if (typeof window.onI18nApply === 'function') window.onI18nApply();
  }

  window.setLang = function (code) {
    if (!T[code]) return;
    currentLang = code;
    localStorage.setItem(LANG_KEY, code);
    applyTranslations();
  };

  window._i18nReady = false;
  document.addEventListener('DOMContentLoaded', function () {
    currentLang = detectLang();
    window._i18nReady = true;
    applyTranslations();
  });
